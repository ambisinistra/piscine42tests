void	ft_swap(int *a, int *b)
{
	int	tmp;

	tmp = *b;
	*b = *a;
	*a = tmp;
}

int main(void)
{
	int a = 0;
	int b = 9;

	printf ("%d%d ", a, b);
	ft_swap(&a, &b);
	printf ("%d%d ", a, b);
	return 0;
}
